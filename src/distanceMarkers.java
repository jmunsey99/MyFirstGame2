public class distanceMarkers {

    public int xpos;
    public int ypos;
    public int width;
    public int height;
    public boolean alive;

    public distanceMarkers (int pXpos, int pYpos, int pWidth, int pHeight){
        xpos = pXpos;
        ypos = pYpos;
        width = pWidth;
        height = pHeight;
        alive = true;

    }

}
